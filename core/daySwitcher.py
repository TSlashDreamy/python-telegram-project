def switch_day(inner):
    match inner:
        case 0:
            return "\nMonday\n================================="

        case 1:
            return "\nTuesday\n================================="

        case 2:
            return "\nWednesday\n================================="

        case 3:
            return "\nThursday\n================================="

        case 4:
            return "\nFriday\n================================="
